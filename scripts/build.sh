#!/bin/bash

# ============================  Script Variables   ============================
SOURCE_FOLDER="../src"
PUBLIC_FOLDER="../dist"

# =============================================================================

timestampString() {
  date +"%Y-%m-%d %T"
}

log() {
  echo "$(timestampString): $1"
}

# =============================================================================

log "================================================================================"
log "BART Watch - Build is starting"
log "================================================================================"

log ">> Removing prev. build."
rm -rf $PUBLIC_FOLDER

log ">> Building bartwatch"
npm run build

log "================================================================================"
log ">> Done!"
log "================================================================================"
log ""
log ">> Public build is in: $PUBLIC_FOLDER"
log ""
